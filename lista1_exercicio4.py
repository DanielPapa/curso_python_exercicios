# EXERCÍCIO 04 #

def soma(n1,n2,n3,n4):
    print(n1+n2+n3+n4)

def subtrair(n1,n2,n3,n4):
    print(n1-n2-n3-n4)

def multiplica(n1,n2,n3,n4):
    print(n1*n2*n3*n4)

def divisao(n1,n2,n3,n4):
    print(n1/n2/n3/n4)

#execução das funções
soma(10,30,100,35)
subtrair(30,20,2,4)
multiplica(10,2,2,1)
divisao(20,4,2,1)
