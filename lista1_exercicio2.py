n1 = input ("Digite um valor para n1: ")
n2 = input ("Digite um valor para n2: ")

n1 = float(n1)
n2 = float(n2)

soma = (n1 + n2)
print (f' A soma das variáveis digitadas é igual a {soma}')
subtração = (n1 - n2)
print (f' A subtração das variáveis digitadas é igual a {subtração}')
divisão = (n1 / n2)
print (f' A divisão das variáveis digitadas é igual a {divisão}')
multiplicação = (n1 * n2)
print (f' A multiplicação das variáveis digitadas é igual a {multiplicação}')

print ("Programa encerrado")


